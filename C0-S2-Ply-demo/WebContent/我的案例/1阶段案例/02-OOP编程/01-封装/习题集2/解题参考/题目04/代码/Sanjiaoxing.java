public class Sanjiaoxing {
	//定义属性
	private int a;
	private int b;
	private int c;

	//getter、setter方法
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}

	//成员方法
	public int Zhouchang(int a, int b, int c) {
		return a + b + c;
	}
}