public class Juxing {
	private double chang;
	private double kuan;

	public double getChang() {
		return chang;
	}

	public void setChang(double chang) {
		this.chang = chang;
	}

	public double getKuan() {
		return kuan;
	}

	public void setKuan(double kuan) {
		this.kuan = kuan;
	}

	public double area() {
		return chang * kuan;
	}
}